    private void txtKodeBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtKodeBarangActionPerformed
        try {
            // 1. Query untuk mencari barang berdasarkan kode yang diketik kasir
            String sql = "SELECT * FROM barang WHERE kode_barang=?";
            java.sql.PreparedStatement ps = koneksi.prepareStatement(sql);
            ps.setString(1, txtKodeBarang.getText());
            java.sql.ResultSet rs = ps.executeQuery();
            
            // 2. Jika barang ditemukan di database
            if (rs.next()) {
                txtNamaBarang.setText(rs.getString("nama_barang")); // Ambil kolom nama_barang
                txtHarga.setText(rs.getString("harga"));           // Ambil kolom harga
                txtJumlah.requestFocus();                          // Kursor otomatis pindah ke Jumlah Beli
            } else {
                // 3. Jika barang tidak ada
                JOptionPane.showMessageDialog(this, "Barang dengan kode tersebut tidak ditemukan!");
                txtKodeBarang.setText("");
                txtNamaBarang.setText("");
                txtHarga.setText("");
                txtKodeBarang.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Eror saat mengambil data barang: " + e.getMessage());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_txtKodeBarangActionPerformed

