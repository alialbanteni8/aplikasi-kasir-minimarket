    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if (tblKeranjang.getRowCount() == 0 || txtBayar.getText().equals("") || txtKembalian.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Transaksi belum lengkap, bre! Selesaikan pembayaran dulu.");
            return;
        }

        String noFaktur = txtNoFaktur.getText();
        String tanggal = txtTanggal.getText();
        String totalBayar = txtTotalBayar.getText();

        try {
            String sqlTransaksi = "INSERT INTO transaksi (no_faktur, tgl_transaksi, id_petugas, total_bayar) VALUES (?, ?, ?, ?)";
            java.sql.PreparedStatement psTransaksi = koneksi.prepareStatement(sqlTransaksi);
                psTransaksi.setString(1, noFaktur);
                psTransaksi.setString(2, tanggal);
                psTransaksi.setInt(3, 1);
                // PERBAIKAN: Menggunakan setInt karena total_bayar bertipe int di database
                psTransaksi.setInt(4, Integer.parseInt(totalBayar)); 
                psTransaksi.executeUpdate();
                
        String sqlDetail = "INSERT INTO detail_transaksi (no_faktur, kode_barang, jumlah, subtotal) VALUES (?, ?, ?, ?)";
         java.sql.PreparedStatement psDetail = koneksi.prepareStatement(sqlDetail);

            int jumlahBaris = tblKeranjang.getRowCount();
              for (int i = 0; i < jumlahBaris; i++) {
                  String kodeBarang = tblKeranjang.getValueAt(i, 0).toString();
                  // Variabel Harga tidak perlu diambil karena tidak disimpan di tabel detail_transaksi database
                  String jumlah      = tblKeranjang.getValueAt(i, 3).toString();
                  String subtotal    = tblKeranjang.getValueAt(i, 4).toString();
               psDetail.setString(1, noFaktur);
               psDetail.setString(2, kodeBarang);
               psDetail.setInt(3, Integer.parseInt(jumlah));    // Menggunakan setInt untuk kolom jumlah
               psDetail.setInt(4, Integer.parseInt(subtotal));  // Menggunakan setInt untuk kolom subtotal
        
               psDetail.executeUpdate();
        }
              

            // Sukses! Kasih notifikasi ke kasir
            JOptionPane.showMessageDialog(this, "Transaksi Berhasil Disimpan ke Database, Bre!");

            // Sesi C: Reset Form biar siap buat transaksi baru
            DefaultTableModel modelKeranjang = (DefaultTableModel) tblKeranjang.getModel();
            modelKeranjang.setRowCount(0); // Kosongkan tabel keranjang
            txtBayar.setText("");
            txtTotalBayar.setText("");
            txtKembalian.setText("");
            
            // Perbarui nomor faktur otomatis biar naik jadi TR0002, TR0003, dst.
            txtNoFaktur.setText(buatNoFaktur()); 
            txtKodeBarang.requestFocus();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan transaksi: " + e.getMessage());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
try {
            // 1. Validasi dulu, pastikan kasir udah ngisi kode barang sama jumlah beli
            if (txtKodeBarang.getText().equals("") || txtJumlah.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Silakan pilih barang dan isi Jumlah Beli dulu, bre!");
                return;
            }
            
            // 2. Ambil data angka dari textfield untuk dihitung subtotalnya
            int harga = Integer.parseInt(txtHarga.getText());
            int jumlah = Integer.parseInt(txtJumlah.getText());
            int subtotal = harga * jumlah;
            
            // 3. Masukkan datanya jadi baris baru di tabel keranjang
            model.addRow(new Object[]{
                txtKodeBarang.getText(),
                txtNamaBarang.getText(),
                txtHarga.getText(),
                txtJumlah.getText(),
                subtotal
            });
            
            // 4. Panggil fungsi hitung total belanjaan (kita buat methodnya di bawah)
            hitungTotal();
            
            // 5. Bersihkan kolom inputan barang biar kasir bisa ngetik barang selanjutnya
            txtKodeBarang.setText("");
            txtNamaBarang.setText("");
            txtHarga.setText("");
            txtJumlah.setText("");
            
            // 6. Kembalikan kursor fokus otomatis ke Kode Barang lagi
            txtKodeBarang.requestFocus();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jumlah beli wajib pakai angka valid ya, cuy!");
            txtJumlah.requestFocus();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Eror: " + e.getMessage());
        }       // TODO add your handling code here:
    }//GEN-LAST:event_btnTambahActionPerformed

    private void hitungTotal() {
        int jumlahBaris = tblKeranjang.getRowCount();
        int totalBiaya = 0;
        
        // Melakukan perulangan untuk menjumlahkan semua angka di kolom Subtotal (indeks ke-4)
        for (int i = 0; i < jumlahBaris; i++) {
            totalBiaya += Integer.parseInt(tblKeranjang.getValueAt(i, 4).toString());
        }
        
        // Menampilkan hasil penjumlahan ke kolom Total Bayar di bagian bawah
        txtTotalBayar.setText(String.valueOf(totalBiaya));
    }
