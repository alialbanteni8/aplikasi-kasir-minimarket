    private String buatNoFaktur() {
        try {
            String sql = "SELECT no_faktur FROM transaksi ORDER BY no_faktur DESC LIMIT 1";
            java.sql.Statement st = koneksi.createStatement();
            java.sql.ResultSet rs = st.executeQuery(sql);
            if (rs.next()) {
                String noFaktur = rs.getString("no_faktur").substring(2);
                int AN = Integer.parseInt(noFaktur) + 1;
                return "TR" + String.format("%04d", AN);
            } else {
                return "TR0001";
            }
        } catch (Exception e) {
            return "TR0001";
        }
    }
