    private void txtBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBayarActionPerformed
        try {
            // 1. Ambil angka dari Total Bayar dan Cash
            int totalBayar = Integer.parseInt(txtTotalBayar.getText());
            int cash = Integer.parseInt(txtBayar.getText()); // Sesuaikan nama variabel txtCash lu
            
            // 2. Validasi kalau uang kasir ternyata kurang
            if (cash < totalBayar) {
                JOptionPane.showMessageDialog(this, "Waduh bre, uang pembayaran kurang!");
                txtKembalian.setText("0");
                return;
            }
            
            // 3. Hitung rumus kembalian
            int kembalian = cash - totalBayar;
            
            // 4. Set hasilnya ke text field kembalian
            txtKembalian.setText(String.valueOf(kembalian)); // Sesuaikan nama variabel txtKembalian lu
            
            // 5. Pindahkan fokus kursor langsung ke tombol SIMPAN TRANSAKSI
            btnSimpan.requestFocus(); // Sesuaikan nama variabel tombol simpan lu
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Input nominal uang bayar yang valid dong, cuy!");
            txtBayar.requestFocus();
        }        // TODO add your handling code here:
    }//GEN-LAST:event_txtBayarActionPerformed

